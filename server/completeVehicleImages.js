// Function to get images for a specific vehicle and color
const getVehicleImages = (vehicleName, color) => {
  try {
    return completeVehicleImages[vehicleName]?.[color] || defaultImages;
  } catch (error) {
    console.error('Error getting vehicle images:', error);
    return defaultImages;
  }
};

// Function to get all colors for a specific vehicle
const getVehicleColors = (vehicleName) => {
  try {
    return Object.keys(completeVehicleImages[vehicleName] || {});
  } catch (error) {
    console.error('Error getting vehicle colors:', error);
    return [];
  }
};

// Function to get all vehicles
const getAllVehicles = () => {
  try {
    return Object.keys(completeVehicleImages);
  } catch (error) {
    console.error('Error getting all vehicles:', error);
    return [];
  }
};

