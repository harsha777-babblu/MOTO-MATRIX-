// Utility functions
const getVehicleImages = (vehicleName, color) => {
  try {
    return allVehicleImages[vehicleName]?.[color] || [];
  } catch (error) {
    console.error('Error getting vehicle images:', error);
    return [];
  }
};

const getVehicleColors = (vehicleName) => {
  try {
    return Object.keys(allVehicleImages[vehicleName] || {});
  } catch (error) {
    console.error('Error getting vehicle colors:', error);
    return [];
  }
};

const getAllVehicles = () => {
  try {
    return Object.keys(allVehicleImages);
  } catch (error) {
    console.error('Error getting all vehicles:', error);
    return [];
  }
};

module.exports = {
  allVehicleImages,
  getVehicleImages,
  getVehicleColors,
  getAllVehicles
};
