// Function to get images for a specific vehicle and color
const getVehicleImages = (brand, model, color) => {
  try {
    return vehicleImages[brand]?.[model]?.[color] || [];
  } catch (error) {
    console.error('Error getting vehicle images:', error);
    return [];
  }
};

// Function to get all colors for a specific vehicle
const getVehicleColors = (brand, model) => {
  try {
    return Object.keys(vehicleImages[brand]?.[model] || {});
  } catch (error) {
    console.error('Error getting vehicle colors:', error);
    return [];
  }
};

// Function to get all models for a specific brand
const getBrandModels = (brand) => {
  try {
    return Object.keys(vehicleImages[brand] || {});
  } catch (error) {
    console.error('Error getting brand models:', error);
    return [];
  }
};

module.exports = {
  vehicleImages,
  getVehicleImages,
  getVehicleColors,
  getBrandModels
};
