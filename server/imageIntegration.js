// Utility functions for image management
const getVehicleImages = (vehicleName, color) => {
  try {
    return vehicleImageDatabase[vehicleName]?.[color] || [];
  } catch (error) {
    console.error('Error getting vehicle images:', error);
    return [];
  }
};

const getVehicleColors = (vehicleName) => {
  try {
    return Object.keys(vehicleImageDatabase[vehicleName] || {});
  } catch (error) {
    console.error('Error getting vehicle colors:', error);
    return [];
  }
};

const getAllVehicles = () => {
  try {
    return Object.keys(vehicleImageDatabase);
  } catch (error) {
    console.error('Error getting all vehicles:', error);
    return [];
  }
};

// Function to get default images for a vehicle (first color)
const getDefaultVehicleImages = (vehicleName) => {
  try {
    const colors = getVehicleColors(vehicleName);
    if (colors.length > 0) {
      return getVehicleImages(vehicleName, colors[0]);
    }
    return [];
  } catch (error) {
    console.error('Error getting default vehicle images:', error);
    return [];
  }
};

module.exports = {
  vehicleImageDatabase,
  getVehicleImages,
  getVehicleColors,
  getAllVehicles,
  getDefaultVehicleImages
};
